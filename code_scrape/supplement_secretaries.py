# Update existing data
for prov, sec_list in SUPPLEMENT.items():
    formatted = []
    for s in sec_list:
        formatted.append({
            "name": s["name"],
            "term": s["term"],
            "role_title": "",
            "note": None,
        })

    if prov in existing:
        old_count = len(existing[prov].get("secretary_list", []))
        # Replace if supplement has more entries (more complete)
        if len(formatted) > old_count:
            existing[prov]["secretary_list"] = formatted
            existing[prov]["source"] = "manual_supplement"
            print(f"✓ {prov}: {old_count} → {len(formatted)} secretaries (replaced)")
        else:
            print(f"  {prov}: kept existing {old_count} (supplement has {len(formatted)})")
    else:
        existing[prov] = {
            "province": prov,
            "source": "manual_supplement",
            "secretary_list": formatted,
        }
        print(f"✓ {prov}: added {len(formatted)} secretaries (new)")

# Save updated file
(DATA_DIR / "wiki_secretaries_clean.json").write_text(
    json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8"
)

# Summary
